# Задание-3:
# Дан список, заполненный произвольными числами.
# Получить список из элементов исходного, удовлетворяющих следующим условиям:
# + Элемент кратен 3
# + Элемент положительный
# + Элемент не кратен 4

print("\nЗадание-3.\n")

Lst_g3 = [random.randint(-99, 99) for _ in range(20)]

Lst_3cond = [el for el in Lst_g3 if el%3 == 0 and el > 0 and el%4 != 0]
print('   Lst_g3 = {} \nLst_3cond = {}'.format(Lst_g3, Lst_3cond))
