# Задание-1:
# Дан список, заполненный произвольными целыми числами.
# Получить новый список, элементы которого будут
# квадратами элементов исходного списка
# [1, 2, 4, 0] --> [1, 4, 16, 0]

import random

print("Задание-1.\n")

Lst_g = [random.randint(-99, 99) for _ in range(20)]
Lst_sq = [el ** 2 for el in Lst_g]
print(' Lst_g = {} \nLst_sq = {}'.format(Lst_g, Lst_sq))

